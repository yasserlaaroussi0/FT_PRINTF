#include "ft_printf.h"

void    ft_putstr(char *str, int *cp)
{
    int i;
    i = 0;
    if (!str)
    {
        *cp += write(1, "(null)", 6);
        return ;
    }
    while (str[i])
    {
        *cp += write(1, &str[i],1);
        i++;
    }
}
